#include<iostream>
using namespace std;

int main(){
    int a;
    cout<<"Size of int is ="<<sizeof(a)<<endl;

    float b;
    cout<<"Size of float is="<<sizeof(b)<<endl;

    double c;
    cout<<"Size of double is="<<sizeof(c)<<endl;

    char d;
    cout<<"Size of character is="<<sizeof(d)<<endl;

    bool e;
    cout<<"Size of bool is="<<sizeof(e)<<endl;

    short int si;
    long int li;
    cout<<"Size of short int is"<<sizeof(si)<<endl;
    cout<<"Size of long int is"<<sizeof(li);


}